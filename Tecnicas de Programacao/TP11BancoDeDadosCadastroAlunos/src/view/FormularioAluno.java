    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        if (txtRGM.getText().equals("")) {
            txtRGM.setText("Preencha o campo");
        
        } else {

            Aluno aluno = new AlunoDAO().pesquisa(Integer.parseInt(txtRGM.getText()));
            if (aluno == null) {
                txtNome.setText("");
                txtNota1.setText("");
                txtNota2.setText("");
                txtMensagemFinal.setText("Aluno n√£o Encontrado");
            } else {
                txtNome.setText(aluno.getNome());
                txtNota1.setText(aluno.getNota1() + "");
                txtNota2.setText(aluno.getNota2() + "");
                txtMensagemFinal.setText("Aluno Encontrado");
            }
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String resp = new AlunoDAO().gravarAluno(montaaluno());
        txtMensagemFinal.setText(resp);

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        String resp = new AlunoDAO().alterarAluno(montaaluno());
        txtMensagemFinal.setText(resp);
        if (resp.equals("OK")) {
            txtMensagemFinal.setText("Alterado");
        } else {
            txtMensagemFinal.setText((resp));
        }

    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String resp = new AlunoDAO().excluirAluno(Integer.parseInt(txtRGM.getText()));

        if (txtRGM.getText().equals("")) {
            txtMensagemFinal.setText("Preencha o campo RGM");
        } else {

            if (resp.equals("OK")) {
                txtMensagemFinal.setText("Excluido");
            } else {
                txtMensagemFinal.setText((resp));
            }
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

